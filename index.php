        <h1><center>
            Desenvolvimento
        <center></h1><br>
        Warframe é um jogo grátis em terceira pessoa, desenvolvido e publicado pela Digital Extremes. Originalmente foi lançado para o Windows em março de 2013, mais tarde foi lançado para o PlayStation 4, Xbox One e também sairá para a Nintendo Switch em 20 de novembro de 2018. 
        O Warframe teve um início lento devido a revisões moderadas e baixas contagens de jogadores, mas o Digital Extremes trabalhou com fãs dedicados do jogo para continuar a melhorá-lo, ajudando a aumentar sua base de jogadores.
        Cinco anos depois da beta aberta, o jogo já viu mais de 38 milhões de jogadores em 2018 e é um dos títulos mais bem sucedidos da Digital Extremes, com novos conteúdos e modos de jogo adicionados.
