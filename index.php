        <br><br><h1><center>
            Jogabilidade
        <center></h1><br>
        No Warframe, os jogadores controlam os Tennos, uma raça de guerreiros antigos que acordaram de séculos de criogenia que se encontram em um sistema planetário em guerra com diferentes facções. 
        Os Tennos usam Warframes energizados junto com armas poderosas e habilidades para completar missões.
        O jogador escolhe 1 dos 3 primeiros Warframes dos quais estes são a Mag, o Volt e o Excalibur que são como uma "armadura" chamada Warframe, que fornece ao jogador habilidades especiais, armas básicas (uma arma corpo-a-corpo, primária e Secundária) e uma nave. 
        Através do painel de navegação da nave, os jogadores podem selecionar qualquer uma das missões disponíveis para eles. 
        O jogo requer que os jogadores completem certas missões em planetas e luas no sistema solar, para assim poderem ter acesso a novos planetas, missões e recursos.
